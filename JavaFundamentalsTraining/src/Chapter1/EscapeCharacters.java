package Chapter1;

/**
 * @author Kabelo Tlhape
 * This class shows different escape characters
 * '\n' - newline
 * '\t' - tab
 * '\b' - backspace
 */
public class EscapeCharacters {
    public static void main(String[] args) {
        System.out.print("Welcome To Java \bTraining\n");
        System.out.println("Name\tAge\t\tHeight");
        System.out.println("-----------------------------");
        System.out.println("Kabelo\t20\t\t1.80");
        System.out.println("Carol\t22\t\t1.70");
    }
}
