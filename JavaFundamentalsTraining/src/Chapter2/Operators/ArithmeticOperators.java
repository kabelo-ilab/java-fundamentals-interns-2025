package Chapter2.Operators;

/**
 * @author Kabelo Tlhape
 * This class shows different arithmetic operators
 */
public class ArithmeticOperators {
    public static void main(String[] args) {
        double answer = 25;

        System.out.println(answer + 4);
        System.out.println(answer - 4);
        System.out.println(answer * 4);
        System.out.println(answer / 4);
        System.out.println(answer % 4);
    }
}
